DROP TABLE IF EXISTS `mod_everquest_characters`;
CREATE TABLE `mod_everquest_characters` (
	`guid` INT(10) UNSIGNED NOT NULL DEFAULT '0' COMMENT 'Global Unique Identifier',
	`class` TINYINT(3) UNSIGNED NOT NULL DEFAULT '0',
	`eqclass` TINYINT(3) UNSIGNED NOT NULL DEFAULT '0',
	`level` TINYINT(3) UNSIGNED NOT NULL DEFAULT '0',
	`xp` INT(10) UNSIGNED NOT NULL DEFAULT '0',
	`leveltime` INT(10) UNSIGNED NOT NULL DEFAULT '0',
	`rest_bonus` FLOAT NOT NULL DEFAULT '0',
	`resettalents_cost` INT(10) UNSIGNED NOT NULL DEFAULT '0',
	`resettalents_time` INT(10) UNSIGNED NOT NULL DEFAULT '0',
	`health` INT(10) UNSIGNED NOT NULL DEFAULT '0',
	`power1` INT(10) UNSIGNED NOT NULL DEFAULT '0',
	`power2` INT(10) UNSIGNED NOT NULL DEFAULT '0',
	`power3` INT(10) UNSIGNED NOT NULL DEFAULT '0',
	`power4` INT(10) UNSIGNED NOT NULL DEFAULT '0',
	`power5` INT(10) UNSIGNED NOT NULL DEFAULT '0',
	`power6` INT(10) UNSIGNED NOT NULL DEFAULT '0',
	`power7` INT(10) UNSIGNED NOT NULL DEFAULT '0',
	`talentGroupsCount` TINYINT(3) UNSIGNED NOT NULL DEFAULT '1',
	`activeTalentGroup` TINYINT(3) UNSIGNED NOT NULL DEFAULT '0',
	PRIMARY KEY (`guid`, `eqclass`) USING BTREE,
	INDEX `idx_guid` (`guid`) USING BTREE
);